class CountryEnt {
  final int id;
  final String title;
  final String icon;
  final String iconUrl;
  final String language;
  final bool isActive;
  final String createdAt;
  final String updatedAt;

  CountryEnt({
    required this.id,
    required this.title,
    required this.icon,
    required this.iconUrl,
    required this.language,
    required this.isActive,
    required this.createdAt,
    required this.updatedAt,
  });
}